import Foundation

func sum(a: Int, b: Int) -> Int {
    return a + b
}

func divide(a: Int, b: Int) -> Int {
    return a / b
}

func multiply(a: Int, b: Int) -> Int {
    return a * b
}

func subtract(a: Int, b: Int) -> Int {
    return a - b
}
